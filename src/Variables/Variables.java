package Variables;

public class Variables {

	public static void main(String[] args) {
		// dataType variableName = value;
		int age = 67;
		String name = "Jane";
		double distance = 45.6;
		
		// write syso and then press Ctrl + spacebar to complete the print command.
		System.out.println(name + " is " + age + " years old. She walked " + distance + " meters.");
	}
}
